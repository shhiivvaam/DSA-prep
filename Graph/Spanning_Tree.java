public class Spanning_Tree {
    // ! Spanning Tree :
    // * 1. It is a Subset of a Graph, which has all vertices covered with minimum
    // possible number of edges. Hence, a spanning tree does not have a cycle and it
    // cannot be disconnected. It is always acyclic.
    // * 2. A spanning tree has (n-1) edges, where n is the number of Vertices
    // (nodes).
    // * 3. All possible spanning trees of Graph(G) have same number of edges and
    // vertices.
    // * 4. Adding one edge to a spanning tree makes a Cycle.
    // * 5. Removing one edge from a spanning tree makes is disconnected.

    // ! Minimum Spanning Tree:
    // * In a weighted Graph, a minimum spanning tree is a spanning tree that has
    // minimum weight than all other spanning trees of the same graph.

    // ? To find this MST:
    // We have two Algorithms: ( both of these are Greedy Algorithms )
    // * 1. Kruskal's Algorithm
    // * 2. Prim's Algorithm
}
